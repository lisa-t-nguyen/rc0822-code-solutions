// this function is being defined with getFirstChar and is being called with
// one parameter that is string. In the function's code block, I want to return
// the first character of any value that is being passed through the parameter string
// doing that, I will use the charAt method for the string property at [0]
// that will give me the first character of any value passed through string

function getFirstChar(string) {
  return string.charAt([0]);
}
